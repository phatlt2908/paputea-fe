import Head from "next/head";
import TutorList from "@/components/tutor/tutor-list";
import tutorApi from "@/services/tutorApi";

export async function getServerSideProps() {
  const data = await loadTutorList();

  return {
    props: { data },
  };
}

const loadTutorList = async () => {
  try {
    const res = await tutorApi.getTutorList({
      pagination: {
        itemsPerPage: 10,
        currentPage: 1,
      },
    });
    return res.data.tutorList;
  } catch (err) {
    console.error(err);
    return null;
  }
};

export default function TutorTeaching({ data }) {
  return (
    <>
      <Head>
        <title>Paputea | Gia sư hiện có</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.png" />
      </Head>

      <div className="container">
        <TutorList tutorList={data} />
      </div>
    </>
  );
}
